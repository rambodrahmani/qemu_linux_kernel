#-------------------------------------------------------------------------------
.DATA
.ALIGN   65536
#-------------------------------------------------------------------------------
.GLOBAL  buf_rdma             # memory buffer to store data retrieved by go_rdma
#-------------------------------------------------------------------------------
buf_rdma:
    .space  128*512

#-------------------------------------------------------------------------------
.TEXT
.GLOBAL  go_rdma
#-------------------------------------------------------------------------------
go_rdma:
    int  $252                 # invoke interrupt 252
    ret
#*******************************************************************************

