#-------------------------------------------------------------------------------
.TEXT
#-------------------------------------------------------------------------------
.EXTERN c_driver_timer
#-------------------------------------------------------------------------------
.GLOBAL a_driver_timer
#-------------------------------------------------------------------------------
a_driver_timer:
    pushal
    call   c_driver_timer
    movl   $0, 0xFEE000B0  # End Of Interrupt
    popal
    iret
