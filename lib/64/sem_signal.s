#         Created on 16/07/2019.
#*******************************************************************************

#-------------------------------------------------------------------------------
.TEXT
#-------------------------------------------------------------------------------
.GLOBAL sem_signal
#-------------------------------------------------------------------------------
sem_signal:
    movb  $1, sem(%edi)     # set the semaphore indexed in %rdi to 1
    ret
#*******************************************************************************

