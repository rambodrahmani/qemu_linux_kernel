#include "def.h"
#-------------------------------------------------------------------------------
.GLOBAL load_gdt
#-------------------------------------------------------------------------------
load_gdt:
    lgdt  gdt_pointer         # load new GDT

    movw  $SEL_SYS_TSS, %cx
    ltr   %cx

    retq
